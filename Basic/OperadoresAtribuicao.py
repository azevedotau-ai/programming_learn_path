#Atribuição Básica
x = 5
y = 4
z = 6

#Atribuição com operadores compostos
x += 5 # x = x + 5
y *= 4 # y = y * 4
z //= 2 # z = z // 2

#Atribuição com expressões
w = y = x = z = 0

#Atribuição com operadores de atribuição multiplos
a , b , c = 5,6,7 # a = 5 , b = 6, c = 7

#Atribuição com unpacking de sequência
lista = [10, 20, 30]
l, s , d = lista # l = 10 , s = 20, d = 30