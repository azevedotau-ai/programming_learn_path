#Atribuindo um literal inteiro para uma variável
idade = 25

#Atribuindo um literal real para uma variável
altura = 1.76

#Imprimindo uma variável inteira
print("Idade = ", idade)

#Imprimindo uma variável real
print("Altura = ",altura)

#Atribuindo um literais booleanos em variáveis
verdadeiro = True
falso = False

#Imprimindo Literais Booleanos
print("Verdadeiro = ", verdadeiro)
print("Falso = ", falso)

#Atribuindo um literal string para uma variável
saudacao = "Ola, Pessoal"

#Juntando literais strings
nome = "Azevedo"
sobrenome = "Tau"
nomecompleto = nome + " " + sobrenome

#Imprimindo Literais Strings
print("Saudação = ", saudacao)
print("Primeiro Nome = ", nome)
print("Ultimo Nome = ", sobrenome)
print("Nome Completo = ", nomecompleto)

#Atribuindo Literal de Lista em uma Variável
minha_lista = [1,2,3,4,5]

#Atribuindo literal de tupla em uma variavel
minha_tupla = (1,2,3,4,5)

#Atribuindo literal de conjunto para uma variável
meu_conjunto = {1, 2, 3, 4}

#Imprimindo literais de Sequencias
print("Lista = ", minha_lista)
print("Tupla = ", minha_tupla)
print("Conjunto = ", meu_conjunto)

#Atribuindo literal de Dicionario a uma variavel
meu_dicionario = {"nome":"Azevedo", "idade": 25, "cidade": "Luanda"}

#Imprimindo Literal Dicionario
print("Dicionario = ", meu_dicionario)
