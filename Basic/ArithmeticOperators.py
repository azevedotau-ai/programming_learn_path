#Operador de Adicao + 
a = 20
b = 30
c = a + b
print(f"A Soma de {a} + {b} = ", c)

#Operador de Subtracao -
e = 30
f = 20
g = e - f
print(f"A Subtracao de {e} - {f} = ", g)

#Operador de multiplicacao *
d = 30
s = 10
ml = d * s
print(f"A Multiplicacao de {d} * {s} = ", ml)

#Operador de Divisao /
dv = 30
dl = 10
dr = dv / dl
print(f"A Divisao de {dv} / {dl} = ", dr)

#Operador de Exponenciacao **
ev = 30
er = 3
err = ev**er
print(f"A Exponenciacao de {ev}**{er} = ", err)

#Operador Modulo //
mv = 50
mr = 5
mrr = mv%mr
print(f"O modulo de {mv} // {mr} = ", mrr)